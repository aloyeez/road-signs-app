// Helper function to get signs by category
export const getSignsByCategory = (category: string): RoadSign[] => {
  if (category === 'Random' || category === 'Náhodná') {
    return czechRoadSigns;
  }
  return czechRoadSigns.filter(sign => sign.category === category);
};

// Helper function to get all unique categories
export const getCategories = (): string[] => {
  const categories = Array.from(new Set(czechRoadSigns.map(sign => sign.category)));
  return ['Random', ...categories];
};

// Helper function to get a sign by ID
export const getSignById = (id: number): RoadSign | undefined => {
  return czechRoadSigns.find(sign => sign.id === id);
};
